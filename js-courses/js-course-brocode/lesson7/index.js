// math

let x = 3.14;
let y = 5, z = 9;
let maximun, minimum;

console.log("x value:", x);

console.log("Math round:", Math.round(x));
console.log("Math floor:", Math.floor(x));
console.log("Math ceil:", Math.ceil(x));
console.log("Math pow:", Math.pow(x, 2));
console.log("Math square root:", Math.sqrt(x));
console.log("Math absolute value:", Math.abs(x));

maximun = Math.max(x, y, z);
minimum = Math.min(x, y, z);

console.log("The maximun value is:", maximun);
console.log("The minimum value is:", minimum);

